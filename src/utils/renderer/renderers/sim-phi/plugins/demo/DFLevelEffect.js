export function loadMod() {
    return time => {
        // console.clear();
        const attr = getAttr(time);
        // for (const [key, value] of Object.entries(attr)) //console.log(`${key}: ${value}`);
        if (attr.blackBgOpacity != null) {
            hook.tmps.customBackDraw = ctxos => {
                ctxos.globalAlpha = attr.blackBgOpacity;
                ctxos.fillRect(0, 0, hook.app.canvasos.width, hook.app.canvasos.height);
            };
        }
        hook.filterOptions = attr;
        if (time > 95) {
            hook.tmps.level = "";
            hook.tmps.name = "";
            hook.tmps.showStat = false;
            hook.tmps.progress = -1;
        }
        if (time > 125.2) {
            hook.tmps.combo = "";
            hook.tmps.combo2 = "";
        }
        if (time > 125.2 && time < 150) {
            const countDown = attr.countDownTime;
            const countDownSec = String(Math.floor(countDown)).padStart(2, "0");
            const countDownCentiSec = String(Math.floor(countDown * 100) % 100).padStart(2, "0");
            let countDownText = "";
            if (countDown > 10) countDownText = `00:${countDownSec}`;
            else countDownText = `${countDownSec}.${countDownCentiSec}`;
            const { wlen, lineScale } = hook.app;
            hook.tmps.customForeDraw = ctxos => {
                ctxos.globalAlpha = 1;
                ctxos.fillStyle = "#fff";
                ctxos.fillRect(
                    wlen - lineScale * 1.625,
                    lineScale * 0.25,
                    lineScale * 3.25,
                    lineScale * 1.75
                );
                ctxos.fillStyle = "#000";
                ctxos.textAlign = "center";
                ctxos.font = `${lineScale * 1.0}px Saira`;
                ctxos.fillText(countDownText, wlen, lineScale * 1.15);
                ctxos.font = `${lineScale * 0.4}px Saira`;
                ctxos.fillText("COUNTDOWN", wlen, lineScale * 1.73);
            };
        }
    };
}
